// Controller 1/2, Stick L/R, Axis X/Y
#define C1RY						                vexRT[Ch2]
#define C1LY						                vexRT[Ch3]
#define C1RX							              vexRT[Ch1]
#define C1LX								            vexRT[Ch4]

#define liftDown 800
#define liftUp 2150
#define lift_pCoeff 0.2

#define MAX_POWER_OUT										127
#define MIN_POWER_OUT								  	-127

int actualLiftAngle;
int desiredLiftAngle;
int liftPowerOut;
int errorLiftAngle;
float liftIntegral;
const float integralLimit = 100;	// with respect to input
bool liftToggle;

int limitMotorPower(int power)																														//
{																																													//
	int	outputPower;																																				//
	//
	outputPower = power;																																		//
	if(outputPower > MAX_POWER_OUT)																													//
	{																																												//
		outputPower = MAX_POWER_OUT;																													//
	}																																												//
	else if(outputPower < MIN_POWER_OUT)																										//
	{																																												//
		outputPower = MIN_POWER_OUT;																													//
	}																																												//
	return(outputPower);																																		//
}

task liftControl()
{
	float kp = 0.2;
	float ki = .7;

	while(true)
	{
		if(liftToggle == true)
		{
			hogCPU();
			actualLiftAngle = SensorValue[pot];
			errorLiftAngle = (desiredLiftAngle - actualLiftAngle);

			// integral term
			if (abs(errorLiftAngle) < integralLimit) {
				liftIntegral += errorLiftAngle;
				} else {
				liftIntegral = 0;
			}
			liftPowerOut = (errorLiftAngle * lift_pCoeff);
			liftPowerOut = (kp * errorLiftAngle);// + (ki * liftIntegral);
			liftPowerOut = limitMotorPower(liftPowerOut);
			motor[liftMotor] = liftPowerOut;
			releaseCPU();
		}
		else
		{
			motor[liftMotor] = 0;
		}
		wait1Msec(20);
	}
}
void setLiftAngle(int liftAngle)																													//
{																																													//
	hogCPU();																																								//
	desiredLiftAngle = liftAngle;																														//
	liftIntegral = 0;																																				//
	releaseCPU();																																						//
}

//H-Drive using 4 Inputs
task hDrive(){
	while (true)
	{
		motor[leftMotorF]  = (C1LY - C1RX)*.8;
		motor[rightMotorF] = (C1LY + C1RX)*.8;
		motor[leftMotorR]  = (C1LY - C1RX)*.8;
		motor[rightMotorR] = (C1LY + C1RX)*.8;

		//Center Wheel Control
		motor[hMotor1] = (vexRT[Btn5U] - vexRT[Btn6U])*127;
		motor[hMotor2] = (vexRT[Btn5U] - vexRT[Btn6U])*127;
		// Motor values can only be updated every 20ms
		wait1Msec(20);
	}
}

task main()
{
	startTask(hDrive);
	startTask(liftControl);
	setLiftAngle(liftUp);
	liftToggle = true;
	while (true)
	{
		if(vexRT[Btn8U]==1)
		{
			setLiftAngle(liftUp);
		}
		else if(vexRT[Btn8D]==1){
			setLiftAngle(liftDown);
		}
		// Motor values can only be updated every 20ms
		wait1Msec(20);
	}
}
