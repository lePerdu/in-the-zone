int limitMotorPower(int power)																														//
{																																													//
	int	outputPower;																																				//
	//
	outputPower = power;																																		//
	if(outputPower > MAX_POWER_OUT)																													//
	{																																												//
		outputPower = MAX_POWER_OUT;																													//
	}																																												//
	else if(outputPower < MIN_POWER_OUT)																										//
	{																																												//
		outputPower = MIN_POWER_OUT;																													//
	}																																												//
	return(outputPower);																																		//
}

task liftControl()
{
	float kp = 0.2;
	float ki = .7;

	while(true)
	{
		if(liftToggle == true)
		{
			hogCPU();
			actualLiftAngle = SensorValue[pot];
			errorLiftAngle = (desiredLiftAngle - actualLiftAngle);

			// integral term
			if (abs(errorLiftAngle) < integralLimit) {
				liftIntegral += errorLiftAngle;
				} else {
				liftIntegral = 0;
			}
			liftPowerOut = (errorLiftAngle * lift_pCoeff);
			liftPowerOut = (kp * errorLiftAngle);// + (ki * liftIntegral);
			liftPowerOut = limitMotorPower(liftPowerOut);
			motor[liftMotor] = liftPowerOut;
			releaseCPU();
		}
		else
		{
			motor[liftMotor] = 0;
		}
		wait1Msec(20);
	}
}
